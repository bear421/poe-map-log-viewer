const SANCTUM_A2 = ZI("Trial of the Sekhemas (A2)", 2, 22, []);
const SANCTUM_A4 = ZI("Trial of the Sekhemas (A4)", 4, 40, []);

export function getZoneInfo(areaName: string, areaLevel?: number): ZoneInfo | undefined {
    const zi = ZONE_TABLE_POE2[areaName] || ZONE_TABLE_POE1[areaName];
    if (zi) return zi;

    if (areaLevel && areaName.startsWith("Sanctum_")) {
        if (areaLevel <= 22) {
            return SANCTUM_A2;
        } else if (areaLevel <= 40) {
            return SANCTUM_A4;
        }
    }
    return undefined;
}

export function getGameVersion(areaName: string): 1 | 2 | undefined {
    if (ZONE_TABLE_POE2[areaName]) return 2;

    if (ZONE_TABLE_POE1[areaName]) return 1;

    return undefined;
}
